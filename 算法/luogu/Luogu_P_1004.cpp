/*
=== 状态空间建模通用思路：从“决策”到“状态” ===

1. 什么是“状态” (State)？
   状态是“决策过程中的一个快照”。它必须包含：
   - 能够唯一标识当前进度的信息。
   - 能够影响未来决策的所有必要历史信息。
   - 核心原则：如果两个场景的“未来可能性”完全一致，那么它们应该被归为同一个“状态”。

2. 建模三步走：
   第一步：确定“决策”是什么？
   - 在本题中，决策是“两条路径每一步往哪走（下或右）”。
   
   第二步：确定“消除后效性”需要哪些信息？
   - 为了知道下一步往哪走，我需要知道：
     a. 当前两条路分别在哪：(x1, y1) 和 (x2, y2)。
     b. 是否已经走过某个格子（因为同一个格子只能取一次）。
   - 巧妙之处：由于两条路是“同时同步”移动的（每步都消耗 1 个单位步数），
     如果两条路在同一时刻到达同一个坐标，它们必然是“相遇”了。
     因此，我们不需要记录“哪些格子被走过”，只需要在计算当前价值时判定 x1==x2 && y1==y2 即可。
   
   第三步：确定“状态维度”：
   - 坐标信息：x1, y1, x2, y2。这 4 个变量完整描述了当前的局势。
   - 状态表示：f[x1][y1][x2][y2] 表示第一条路在 (x1, y1)，第二条路在 (x2, y2) 时能拿到的最大值。

3. 状态空间的优化 (Dimension Reduction)：
   - 寻找隐藏约束：在本题中，x1 + y1 必须等于 x2 + y2（因为步数同步）。
   - 设步数为 k，则 y1 = k - x1, y2 = k - x2。
   - 状态可优化为：f[k][x1][x2]。
   - 建模直觉：每多一个约束，就可以减少一个维度。

4. 如何判断建模是否成功？
   - 完整性：是否所有合法的决策序列都能对应到状态路径上？
   - 独立性：给定一个状态，我是否不需要知道它是“怎么来的”，就能决定“往哪去”？（无后效性）
   - 空间可行性：状态总数（维度乘积）是否在内存允许范围内？（通常 10^7 左右是极限）
*/