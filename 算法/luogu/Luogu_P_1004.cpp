#include<bits/stdc++.h>
using namespace std;
int n, a[11][11], memo[11][11][11][11];

int dfs(int x1, int y1, int x2, int y2) {
    if (x1 < 1 || y1 < 1 || x2 < 1 || y2 < 1) return 0;
    if (x1 == 1 && y1 == 1 && x2 == 1 && y2 == 1) return a[1][1];
    if (~memo[x1][y1][x2][y2]) return memo[x1][y1][x2][y2];

    int res = max({dfs(x1-1, y1, x2-1, y2), dfs(x1, y1-1, x2-1, y2),
                   dfs(x1-1, y1, x2, y2-1), dfs(x1, y1-1, x2, y2-1)});

    res += a[x1][y1] + (x1 == x2 && y1 == y2 ? 0 : a[x2][y2]);
    return memo[x1][y1][x2][y2] = res;
}

int main() {
    memset(memo, -1, sizeof(memo));
    cin >> n;
    int x, y, v;
    while (cin >> x >> y >> v && (x || y || v)) a[x][y] = v;
    cout << dfs(n, n, n, n);
}
